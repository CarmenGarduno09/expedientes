function preguntasA(){
	$('#preguntasA').css('display','block');
	$('#botonA').empty();
	$('#botonA').append('<center><a href="javascript:void(0)" onclick="limpiarA()"><button type="button" class="btn btn-primary btn-xs"><span class="glyphicon glyphicon-chevron-up"></span></button></a></center>');
}
function limpiarA(){
	$('#preguntasA').css('display','none');
	$('#botonA').empty();
	$('#botonA').append('<center><a href="javascript:void(0)" onclick="preguntasA()"><button type="button" class="btn btn-primary btn-xs"><span class="glyphicon glyphicon-chevron-down"></span></button></a><center>');
}

