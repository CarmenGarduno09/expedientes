//Funciones para los input SI NO OTRO. 

function enable_fields(){
	if ($("#verificar").is(":checked")) {
		$('#otro_text').prop('disabled',true).val('');
	   
		$('#numero_acta').prop('disabled',false);
		$('#lugar_r').prop('disabled',false);
		$('#curp').prop('disabled',false);
	}else{
		$('#numero_acta').prop('disabled',true);
		$('#lugar_r').prop('disabled',true);
		$('#curp').prop('disabled',true);
	}
}
function disable_fields(){
	$('#numero_acta').prop('disabled',true);
	$('#lugar_r').prop('disabled',true);
	$('#curp').prop('disabled',true); 
	$("#otro_tex").prop('disabled',false).val('');
}

function enable_fields_otro(){
	if ($("#otro").is(":checked")) {
		$('#otro_text').prop('disabled',false);
		$('#numero_acta').prop('disabled',true);
	   $('#lugar_r').prop('disabled',true);
	   $('#curp').prop('disabled',true); 
   
		
	}else{
		$('#otro_text').prop('disabled',true);
		
	}
}

